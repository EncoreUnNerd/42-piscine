int	ft_sqrt(int nb)
{
	int	i;
	int	sub;

	sub = 1;
	i = 0;
	while (nb > 0)
	{
		nb -= sub;
		i++;
		if (nb == 0)
			return (i);
		if (nb == 1 || nb < 0)
			return (0);
		sub += 2;
	}
	return (0);
}
